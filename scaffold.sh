# ---------------------------------------------------------------------------
# binder/ — Binder config for launching notebooks on mybinder.org
#
# repo2docker would try to `pip install .` if it sees the root pyproject.toml,
# which fails because the root is a virtual workspace (package = false).
# Placing config in binder/ makes repo2docker ignore the root pyproject.toml.
# We use uv to install deps from the single source of truth (pyproject.toml).
# ---------------------------------------------------------------------------
echo "   ↳ binder/"
cat > binder/runtime.txt <<'TXT'
python-3.13
TXT

cat > binder/postBuild <<'BASH'
#!/usr/bin/env bash
set -euo pipefail

# Bootstrap uv (fast, single-binary installer).
pip install --no-cache-dir uv==0.10.9

# Install notebook deps from the single source of truth (pyproject.toml).
uv export --no-hashes --group notebooks | uv pip install --system --no-cache -r -

# Install workspace packages (if any exist yet).
for pkg in packages/*/pyproject.toml; do
    [ -f "$pkg" ] && uv pip install --system --no-cache "./$(dirname "$pkg")"
done
BASH
chmod +x binder/postBuild

# ---------------------------------------------------------------------------
# uv init — workspace root (virtual package, not installable itself)
# Docs: https://docs.astral.sh/uv/concepts/workspaces/
