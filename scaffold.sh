#!/usr/bin/env bash
# scaffold.sh — bootstrap a Python data-science / analytics project
#
# Creates a uv-managed workspace with:
#   packages/  — reusable library code (src layout, uv_build backend)
#   apps/      — runnable apps / CLI entry points
#   notebooks/ — Jupyter exploration
#   docs/      — methodology, metrics, assumptions, limitations, data sources
#   data/      — raw / interim / processed / external (gitignored)
#   outputs/   — figures, map layers, marts (gitignored)
#   .vscode/   — settings, launch, tasks, code-workspace
#   .github/   — CI workflow
#
# Usage:
#   ./scaffold.sh my-project-name
#
# Prerequisites: uv (https://docs.astral.sh/uv/getting-started/installation/)

set -euo pipefail

# ---------------------------------------------------------------------------
# Args
# ---------------------------------------------------------------------------
if [ "$#" -ne 1 ]; then
  echo "Usage: $0 <project-name>"
  echo ""
  echo "Scaffolds a uv-managed Python data-science workspace."
  exit 1
fi

PROJECT="$1"

# Git author info (used for CITATION.cff and GitHub URL)
GIT_NAME="$(git config user.name  || echo 'Your Name')"
GIVEN="${GIT_NAME%% *}"
FAMILY="${GIT_NAME#* }"
[ "$GIVEN" = "$FAMILY" ] && FAMILY=""
GH_USER="$(git config github.user 2>/dev/null || echo "${USER}")"
TODAY="$(date +%Y-%m-%d)"

echo "🚀  Scaffolding ${PROJECT} ..."

# ---------------------------------------------------------------------------
# Top-level directories
# ---------------------------------------------------------------------------
mkdir -p "${PROJECT}"
cd "${PROJECT}"

mkdir -p \
  .github/workflows \
  .vscode \
  apps \
  binder \
  data/{raw,interim,processed,external} \
  docs \
  notebooks \
  outputs/{figures,map_layers} \
  packages \
  tests

# .gitkeep for gitignored empty dirs
for d in data/{raw,interim,processed,external} outputs/{figures,map_layers}; do
  touch "${d}/.gitkeep"
done

